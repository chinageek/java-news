use news;
set names utf8;

DROP TABLE IF EXISTS `match_hints`;

create table `user`(id bigint unsigned auto_increment primary key,
	`name` varchar(16) not null COMMENT '用户名',
	`email` varchar(40) not null COMMENT '邮箱',
	`sex` varchar(6) NULL,
	`age` int NULL
);
COMMENT='用户信息'
COLLATE='utf8_general_ci'
ENGINE=InnoDB;


